    P.PRODUCT_NAME,
    W.WAREHOUSE_NAMES,
    L.ADDRESS,
    I.QUANTITY
FROM INVENTORIES I 
JOIN PRODUCTS P 
    ON P.PRODUCT_ID = I.PRODUCT_ID
JOIN WAREHOUSES W
    ON W.WAREHOUSE_ID = I.WAREHOUSE_ID
JOIN LOCATIONS L
    ON L.LOCATION_ID = W.LOCATION_ID
WHERE
    I.QUANTITY > 0;

