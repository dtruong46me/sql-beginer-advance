SELECT 
    P.PRODUCT_ID,
    P.PRODUCT_NAME,
    COUNT(OI.ORDER_ID) AS SL_DON_HANG
FROM ORDER_ITEMS OI
JOIN PRODUCTS P
    ON OI.PRODUCT_ID = P.PRODUCT_ID
JOIN ORDERS O
    ON O.ORDER_ID = OI.ORDER_ID
WHERE
    1=1
    AND EXTRACT(YEAR FROM O.ORDER_DATE) = 2017
GROUP BY P.PRODUCT_ID, P.PRODUCT_NAME
ORDER BY COUNT(OI.ORDER_ID) DESC;

