SELECT O.CUSTOMER_ID, NAME, SUM(OI.QUANTITY)
FROM ORDERS O
JOIN ORDER_ITEMS OI
    ON O.ORDER_ID = OI.ORDER_ID
JOIN CUSTOMERS C
    ON O.CUSTOMER_ID = C.CUSTOMER_ID
GROUP BY O.CUSTOMER_ID, NAME
HAVING SUM(OI.QUANTITY) > 10;

