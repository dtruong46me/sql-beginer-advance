    P.PRODUCT_NAME,
    W.WAREHOUSE_NAMES,
    I.QUANTITY
FROM INVENTORIES I 
JOIN PRODUCTS P
    ON P.PRODUCT_ID = I.PRODUCT_ID
JOIN WAREHOUSES W
    ON I.WAREHOUSE_ID = W.WAREHOUSE_ID
WHERE
    I.QUANTITY > 0;


