SELECT O.CUSTOMER_ID, NAME, O.ORDER_DATE, OI.QUANTITY
FROM ORDERS O
JOIN ORDER_ITEMS OI
    ON O.ORDER_ID = OI.ORDER_ID
JOIN CUSTOMERS C
    ON O.CUSTOMER_ID = C.CUSTOMER_ID
WHERE
    1=1
    AND OI.QUANTITY > 50
    AND O.STATUS = 'Pending'
    AND O.ORDER_DATE > TO_DATE('10/02/2017', 'DD/MM/YYYY');

